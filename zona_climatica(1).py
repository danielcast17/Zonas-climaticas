# Busqueda
def obtener_zona_climatica(provincia, altitud):
    rangos = zonas_climaticas.get(provincia)
    if not rangos:
        return f"Provincia '{provincia}' no disponible."

    for min_alt, max_alt, zona in rangos:
        if min_alt <= altitud <= max_alt:
            return f"游댍 Zona clim치tica para {provincia} a {altitud} m: {zona}"
    return "No se encontr칩 una zona clim치tica para esa altitud."

# Widgets interactivos
provincia_dropdown = widgets.Dropdown(
    options=list(zonas_climaticas.keys()),
    description='Provincia:',
    style={'description_width': 'initial'}
)

altitud_input = widgets.IntSlider(
    value=100,
    min=0,
    max=2000,
    step=10,
    description='Altitud (m):',
    style={'description_width': 'initial'},
    continuous_update=False
)

output = widgets.Output()

def actualizar_resultado(change=None):
    output.clear_output()
    with output:
        provincia = provincia_dropdown.value
        altitud = altitud_input.value
        print(obtener_zona_climatica(provincia, altitud))

provincia_dropdown.observe(actualizar_resultado, names='value')
altitud_input.observe(actualizar_resultado, names='value')

# Resultado
display(provincia_dropdown, altitud_input, output)
actualizar_resultado()